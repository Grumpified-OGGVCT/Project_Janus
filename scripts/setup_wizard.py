    if ok:
        console.print(
            f"[green]✓ Ollama running[/] — "
            f"{len(models)} model(s) available"
        )
        if models:
            console.print(f"  [dim]{', '.join(models[:5])}[/]")
        console.print()
        return True

    # Not available — warn and offer options
    console.print()
    console.print(Panel(
        f"[bold yellow]⚠ Ollama is not responding[/]\n\n"
        f"  Configured host: [cyan]http://{host}:{port}[/]\n\n"
        "  The AI brain requires a running Ollama server.\n"
        f"  Default port: [cyan]{DEFAULT_PORT}[/]",
        title="[yellow]Ollama Unavailable[/]",
        border_style="yellow",
    ))
    console.print()

    console.print("[bold]Options:[/]")
    console.print(f"  [cyan]1[/] — Start Ollama (port {port})")
    console.print("  [cyan]2[/] — Continue without Ollama (limited mode)")
    console.print("  [cyan]3[/] — Exit")
    console.print()

    choice = Prompt.ask("[cyan]Select[/]", choices=["1", "2", "3"], default="1")

    if choice == "1":
        started = _start_ollama(host, port)
        if started:
            config["auto_start_ollama"] = True
            save_config(config)
        return started
    elif choice == "3":
        console.print("[dim]Exiting.[/]")
        sys.exit(0)

    console.print("[yellow]⚠ Continuing in limited mode.[/]\n")
    return False


