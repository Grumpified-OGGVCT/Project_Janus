# Main Entry Point
# ═══════════════════════════════════════════════════════════

def main() -> None:
    """CLI entry point — routes to full wizard, quick check, or reconfig."""
    parser = argparse.ArgumentParser(
        description="Project Janus — Setup & Configuration Wizard",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=(
            "Examples:\n"
            "  python scripts/setup_wizard.py          # Full setup\n"
            "  python scripts/setup_wizard.py --check  # Quick health check\n"
            "  python scripts/setup_wizard.py --config # Reconfigure Ollama\n"
        ),
    )
    parser.add_argument(
        "--check", action="store_true",
        help="Quick health check and launch (skip full setup)",
    )
    parser.add_argument(
        "--config", action="store_true",
        help="Reconfigure Ollama settings only",
    )
    args = parser.parse_args()

    config = load_config()

    # ── Quick Check Mode ──────────────────────────────────
    if args.check:
        show_banner(minimal=True)
        ollama_ok = quick_check(config)
        config["last_run"] = datetime.now().isoformat()
        save_config(config)
        phase_launch_demo(config, ollama_ok)
        return

    # ── Reconfig Mode ─────────────────────────────────────
    if args.config:
        show_banner(minimal=True)
        config, ollama_ok = phase_ollama_config(config)
        if ollama_ok:
            config = phase_model_check(config, ollama_ok)
        save_config(config)
        console.print("[green]✓ Configuration updated.[/]\n")
        return

    # ── Full Setup Wizard ─────────────────────────────────
    show_banner()

    console.print(
        "[dim]This wizard will configure your Project Janus installation.\n"
        "It checks system requirements, verifies dependencies, configures\n"
        "Ollama for local AI inference, and launches the demo page.[/]\n"
    )

    if not Confirm.ask("[cyan]Ready to begin?[/]", default=True):
        console.print("[dim]Setup cancelled. Run again anytime.[/]")
        return

    console.print()

    # Phase 1: System
    system_info = phase_system_check()

    # Phase 2: Dependencies
    phase_dependency_check()

    # Phase 3: Ollama
    config, ollama_ok = phase_ollama_config(config)

    # Phase 4: Model
    config = phase_model_check(config, ollama_ok)

    # Phase 5: Summary
    config = phase_summary_and_save(config, system_info, ollama_ok)

    # Phase 6: Launch
    phase_launch_demo(config, ollama_ok)


if __name__ == "__main__":
    main()
