def load_config() -> Dict[str, Any]:
    """Load saved configuration or return defaults."""
    defaults: Dict[str, Any] = {
        "ollama_host": DEFAULT_HOST,
        "ollama_port": DEFAULT_PORT,
        "ollama_url": f"http://{DEFAULT_HOST}:{DEFAULT_PORT}",
        "model": DEFAULT_MODEL,
        "auto_start_ollama": False,
        "setup_complete": False,
        "version": VERSION,
        "created": None,
        "last_run": None,
    }
    if CONFIG_FILE.exists():
        try:
            with open(CONFIG_FILE, encoding="utf-8") as f:
                saved = json.load(f)
            defaults.update(saved)
        except (json.JSONDecodeError, IOError):
            pass
    return defaults


def save_config(config: Dict[str, Any]) -> None:
    """Persist configuration to .janus_config.json and .env."""
    config["last_run"] = datetime.now().isoformat()
    if not config.get("created"):
        config["created"] = datetime.now().isoformat()
    config["version"] = VERSION

    with open(CONFIG_FILE, "w", encoding="utf-8") as f:
        json.dump(config, f, indent=2)

    # Write .env for compatibility with scripts and CI
    with open(ENV_FILE, "w", encoding="utf-8") as f:
        f.write("# Project Janus — Generated by Setup Wizard\n")
        f.write(f"# {datetime.now().isoformat()}\n\n")
        f.write(f"OLLAMA_HOST={config['ollama_url']}\n")
        f.write(f"JANUS_MODEL={config['model']}\n")
        f.write(f"JANUS_VERSION={VERSION}\n")
        f.write(f"JANUS_AUTO_START={str(config.get('auto_start_ollama', False)).lower()}\n")


