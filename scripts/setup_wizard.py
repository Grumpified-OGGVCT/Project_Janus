# Phase 4: Model Verification
# ═══════════════════════════════════════════════════════════

def phase_model_check(config: Dict[str, Any], ollama_ok: bool) -> Dict[str, Any]:
    """Verify the configured model is available; offer to pull if not."""
    console.print(Rule("[bold yellow]Phase 4 — Model Verification[/]"))
    console.print()

    if not ollama_ok:
        console.print("[yellow]⚠ Ollama not available — skipping model check.[/]\n")
        return config

    model = config.get("model", DEFAULT_MODEL)
    host = config["ollama_host"]
    port = config["ollama_port"]

    ok, models = _check_ollama(host, port)
    target_base = model.split(":")[0]

    if any(target_base in m for m in models):
        console.print(f"[green]✓ Model '{model}' is available.[/]\n")
        return config

    console.print(f"[yellow]⚠ Model '{model}' not found locally.[/]")
    if models:
        console.print(f"[dim]  Available models: {', '.join(models[:8])}[/]")
    console.print()

    if Confirm.ask(
        f"[cyan]Pull '{model}' now?[/] [dim](may take several minutes)[/]",
        default=True,
    ):
        try:
            console.print(f"[cyan]Pulling {model}...[/]")
            result = subprocess.run(
                ["ollama", "pull", model],
                timeout=600,
            )
            if result.returncode == 0:
                console.print(f"[green]✓ {model} ready.[/]\n")
            else:
                console.print(f"[red]✗ Pull exited with code {result.returncode}.[/]\n")
        except subprocess.TimeoutExpired:
            console.print("[red]✗ Pull timed out after 10 minutes.[/]\n")
        except Exception as e:
            console.print(f"[red]✗ Pull failed: {e}[/]\n")
    else:
        alt = Prompt.ask(
            "[cyan]Use a different model instead? (leave blank to keep current)[/]",
            default="",
        )
        if alt.strip():
            config["model"] = alt.strip()
            console.print(f"[green]✓ Model set to '{alt.strip()}'.[/]\n")

    return config


