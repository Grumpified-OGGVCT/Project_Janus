def _check_ollama(host: str, port: int, timeout: int = 5) -> Tuple[bool, List[str]]:
    """Test connectivity to Ollama. Returns (ok, model_names)."""
    try:
        import requests
        url = f"http://{host}:{port}/api/tags"
        resp = requests.get(url, timeout=timeout)
        if resp.status_code == 200:
            data = resp.json()
            models = [m.get("name", "?") for m in data.get("models", [])]
            return True, models
    except Exception:
        pass
    return False, []


def _start_ollama(host: str, port: int) -> bool:
    """Attempt to start Ollama server as a background process."""
    console.print(f"[yellow]⚡ Starting Ollama on {host}:{port}...[/]")

    ollama_path = shutil.which("ollama")
    if not ollama_path:
        console.print("[red]✗ Cannot find 'ollama' in PATH.[/]")
        console.print("[dim]  Install from: https://ollama.com/download[/]\n")
        return False

    try:
        env = os.environ.copy()
        env["OLLAMA_HOST"] = f"{host}:{port}"

        if platform.system() == "Windows":
            # Windows: detached background process
            subprocess.Popen(
                ["ollama", "serve"],
                env=env,
                stdout=subprocess.DEVNULL,
                stderr=subprocess.DEVNULL,
                creationflags=subprocess.CREATE_NO_WINDOW | subprocess.DETACHED_PROCESS,
            )
        else:
            # Unix: new session
            subprocess.Popen(
                ["ollama", "serve"],
                env=env,
                stdout=subprocess.DEVNULL,
                stderr=subprocess.DEVNULL,
                start_new_session=True,
            )

        # Poll until Ollama is ready (up to 30 seconds)
        with Progress(
            SpinnerColumn(),
            TextColumn("[progress.description]{task.description}"),
            console=console,
        ) as progress:
            task = progress.add_task("Waiting for Ollama...", total=None)
            for i in range(30):
                time.sleep(1)
                ok, _ = _check_ollama(host, port, timeout=2)
                if ok:
                    progress.update(task, description="[green]✓ Ollama is running![/]")
                    time.sleep(0.5)
                    console.print()
                    return True
                progress.update(
                    task, description=f"Waiting for Ollama... ({i + 1}s)"
                )

        console.print("[red]✗ Ollama did not start within 30 seconds.[/]\n")
        return False

    except Exception as e:
        console.print(f"[red]✗ Failed to start Ollama: {e}[/]\n")
        return False


def phase_ollama_config(config: Dict[str, Any]) -> Tuple[Dict[str, Any], bool]:
    """Interactive Ollama configuration with connectivity testing."""
    console.print(Rule("[bold yellow]Phase 3 — Ollama Configuration[/]"))
    console.print()

    host = config.get("ollama_host", DEFAULT_HOST)
    port = config.get("ollama_port", DEFAULT_PORT)

    # Test current/saved connection
    console.print(f"[cyan]Testing Ollama at {host}:{port}...[/]")
    ok, models = _check_ollama(host, port)

    if ok:
        console.print(f"[green]✓ Ollama is running![/]  [{len(models)} model(s)]")
        if models:
            for m in models[:10]:
                console.print(f"  [dim]• {m}[/]")
        console.print()
        config["ollama_host"] = host
        config["ollama_port"] = port
        config["ollama_url"] = f"http://{host}:{port}"
        return config, True

    # Not available — show options
    console.print()
    console.print(Panel(
        "[bold yellow]⚠ Ollama is not responding[/]\n\n"
        f"  Tried: [cyan]http://{host}:{port}[/]\n\n"
        "  Ollama provides the local AI brain for Project Janus.\n"
        "  Install from: [link=https://ollama.com/download]https://ollama.com/download[/]",
        title="[yellow]Connection Failed[/]",
        border_style="yellow",
    ))
    console.print()

    console.print("[bold]Options:[/]")
    console.print(f"  [cyan]1[/] — Start Ollama now (port {port})")
    console.print("  [cyan]2[/] — Configure a different host/port")
    console.print("  [cyan]3[/] — Skip (continue without Ollama)")
    console.print()

    choice = Prompt.ask(
        "[cyan]Select option[/]",
        choices=["1", "2", "3"],
        default="1",
    )

    if choice == "1":
        started = _start_ollama(host, port)
        config["auto_start_ollama"] = started
        config["ollama_host"] = host
        config["ollama_port"] = port
        config["ollama_url"] = f"http://{host}:{port}"
        return config, started

    elif choice == "2":
        new_host = Prompt.ask("[cyan]Ollama host[/]", default=host)
        new_port = IntPrompt.ask("[cyan]Ollama port[/]", default=port)
        console.print(f"\n[cyan]Testing {new_host}:{new_port}...[/]")
        ok, models = _check_ollama(new_host, new_port)
        if ok:
            console.print(f"[green]✓ Connected! {len(models)} model(s) available.[/]\n")
        else:
            console.print("[yellow]⚠ Still not reachable. Config saved — you can start Ollama later.[/]\n")
        config["ollama_host"] = new_host
        config["ollama_port"] = new_port
        config["ollama_url"] = f"http://{new_host}:{new_port}"
        return config, ok

    else:
        console.print("[yellow]⚠ Skipping — demo will run in offline mode.[/]\n")
        config["ollama_host"] = host
        config["ollama_port"] = port
        config["ollama_url"] = f"http://{host}:{port}"
        return config, False


