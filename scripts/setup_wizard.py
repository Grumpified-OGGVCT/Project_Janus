def phase_dependency_check() -> bool:
    """Verify core package installation."""
    console.print(Rule("[bold yellow]Phase 2 — Dependency Verification[/]"))
    console.print()

    table = Table(
        box=box.ROUNDED,
        show_header=True,
        header_style="bold cyan",
        title="Core Packages",
    )
    table.add_column("Package", style="white", width=20)
    table.add_column("Status", justify="center", width=12)
    table.add_column("Version", style="dim")

    missing: List[str] = []

    for pkg_name, import_name in CORE_PACKAGES:
        try:
            mod = __import__(import_name)
            version = getattr(mod, "__version__", getattr(mod, "VERSION", "installed"))
            table.add_row(pkg_name, "[green]✓ OK[/]", str(version))
        except ImportError:
            table.add_row(pkg_name, "[red]✗ MISSING[/]", "—")
            missing.append(pkg_name)

    console.print(table)
    console.print()

    if missing:
        console.print(
            f"[yellow]⚠ {len(missing)} package(s) missing:[/] "
            f"[dim]{', '.join(missing)}[/]"
        )
        console.print()
        if Confirm.ask(
            "[cyan]Install missing packages from requirements.txt?[/]",
            default=True,
        ):
            req_file = PROJECT_ROOT / "requirements.txt"
            if req_file.exists():
                with Progress(
                    SpinnerColumn(),
                    TextColumn("[progress.description]{task.description}"),
                    console=console,
                ) as progress:
                    task = progress.add_task("Installing dependencies...", total=None)
                    result = subprocess.run(
                        [sys.executable, "-m", "pip", "install", "-r", str(req_file)],
                        capture_output=True, text=True,
                    )
                    if result.returncode == 0:
                        progress.update(
                            task, description="[green]✓ Dependencies installed![/]"
                        )
                    else:
                        progress.update(
                            task,
                            description="[yellow]⚠ Some packages may have failed[/]",
                        )
                console.print()
            else:
                console.print("[red]✗ requirements.txt not found![/]")
        console.print()
    else:
        console.print("[green]✓ All core dependencies verified.[/]\n")

    return len(missing) == 0


